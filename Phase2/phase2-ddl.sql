-- Moive tables

create table Actor (
    actor_ID int auto_increment,
    actor_firstname varchar(255),
    actor_lastname varchar(255),
    actor_age int,
    actor_net_worth int,
    primary key (actor_ID)
);

create table Awards (
    award_ID int auto_increment,
    award_name varchar(255),
    primary key (award_ID)
);

create table Director (
    director_ID int auto_increment,
    director_firstname varchar(255),
    director_lastname varchar(255),
    director_age int,
    primary key (director_ID)
);

create table Genre (
    genre_ID int auto_increment,
    genre_title varchar(255),
    primary key (genre_ID)
);

create table Rating (
    rating_ID int auto_increment,
    rating_IMDB DOUBLE,
    rating_Rotten_Tomatoes DOUBLE,
    rating_Metacritic DOUBLE,
    primary key (rating_ID)
);

create table Movies (
    movie_ID int auto_increment,
    movie_title varchar(255),
    release_date date,
    duration int,
    synopsis varchar(4096),
    primary key (movie_ID)
);

create table movieGenre (
    movieGenre_ID int auto_increment,
    movie_ID int,
    genre_ID int,
    primary key (movieGenre_ID),
    foreign key (movie_ID) references Movies(movie_ID) on delete cascade,
    foreign key (genre_ID) references Genre(genre_ID) on delete cascade
);

create table movieRating (
    movieRating_ID int auto_increment,
    movie_ID int,
    rating_ID int,
    primary key (movieRating_ID),
    foreign key (movie_ID) references Movies(movie_ID) on delete cascade,
    foreign key (rating_ID) references Rating(rating_ID) on delete cascade
);

create table movieDirector (
    movieDirector_ID int auto_increment,
    movie_ID int,
    director_ID int,
    primary key (movieDirector_ID),
    foreign key (movie_ID) references Movies(movie_ID) on delete cascade,
    foreign key (director_ID) references Director(director_ID) on delete cascade
);

create table movieAwards (
    movieAwards_ID int auto_increment,
    movie_ID int,
    award_ID int,
    primary key (movieAwards_ID),
    foreign key (movie_ID) references Movies(movie_ID) on delete cascade,
    foreign key (award_ID) references Awards(award_ID) on delete cascade
);

create table movieActor (
    movieActor_ID int auto_increment,
    movie_ID int,
    actor_ID int,
    primary key (movieActor_ID),
    foreign key (movie_ID) references Movies(movie_ID) on delete cascade,
    foreign key (actor_ID) references Actor(actor_ID) on delete cascade
);

-- Triggers and Views and Logger

drop table if exists logger;
create table logger (
    logger_id int not null auto_increment,
    logger_operation varchar(255) not null,
    logger_table varchar(255) not null,
    logger_record_id int not null,
    logger_timestamp datetime default now(),
    primary key (logger_id)
);

drop view if exists movies_by_ID;
create view movies_by_ID as (
    select * from Movies
);

drop trigger if exists rating_update_trigger;
create trigger rating_update_trigger
after update on Rating
for each row
BEGIN 
    insert into logger(logger_operation, logger_table, logger_record_id) values
        ('UPDATE', 'Rating', new.rating_ID);
end;

drop trigger if exists rating_insert_trigger;
create trigger rating_insert_trigger
after update on Rating
for each row
BEGIN
    insert into logger(logger_operation, logger_table, logger_record_id) values
        ('INSERT', 'RATING', new.rating_ID);
end;



